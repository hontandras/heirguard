// Dynamic translation loader
let translationsCache: Record<string, Translation> = {};

export const loadTranslations = async (language: string): Promise<Translation> => {
  // Return cached translation if available
  if (translationsCache[language]) {
    return translationsCache[language];
  }

  try {
    // Dynamically import the JSON file
    const translationModule = await import(`./${language}.json`);
    const translation = translationModule.default;
    
    // Cache the translation
    translationsCache[language] = translation;
    
    return translation;
  } catch (error) {
    console.warn(`Failed to load translation for ${language}, falling back to English`);
    
    // Fallback to English if the requested language fails
    if (language !== 'en') {
      return loadTranslations('en');
    }
    
    // If even English fails, throw error
    throw new Error('Failed to load translations');
  }
};

// Clear cache function (useful for development)
export const clearTranslationCache = () => {
  translationsCache = {};
};